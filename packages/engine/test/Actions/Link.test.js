test('Link with invalid params', async () => {
  const rootBlock = {
    blockId: 'root',
    meta: {
      category: 'context',
    },
    areas: {
      content: {
        blocks: [
          {
            blockId: 'button',
            type: 'Button',
            meta: {
              category: 'display',
              valueType: 'string',
            },
            events: {
              onClick: [{ id: 'a', type: 'Link', params: {} }],
            },
          },
        ],
      },
    },
  };
  const context = testContext({
    rootContext,
    rootBlock,
    pageId,
  });
  const { button } = context.RootBlocks.map;
  const res = await button.triggerEvent({ name: 'onClick' });
  expect(res).toEqual({
    blockId: 'button',
    event: undefined,
    eventName: 'onClick',
    responses: [
      {
        actionId: 'a',
        actionType: 'Link',
        error: new Error('Invalid Link action.'),
      },
    ],
    success: false,
    timestamp: { date: 0 },
  });
});
